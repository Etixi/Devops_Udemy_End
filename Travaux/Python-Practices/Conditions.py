# IF/Else Condition
x = 31

if x < 30:
    print("Inside of block")
    print("x is less than 30")
else:
    print("Inside else block")
    print("x is greater than 30")
print("===="*15)

# IF/Elif/Else Condition
x = 41

if x > 40:
    print("x is greater than 40")
elif x == 40:
    print("x is equal to 40")
else:
    print("x is less than 40")

